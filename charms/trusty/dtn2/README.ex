# Overview

This DTN (delay-tolerant module) modules charms the DTN2 data service implementation of dtnrg hosted at https://sourceforge.net/projects/dtn/files/DTN2.

# Usage
This charm is available in the Juju Charm Store, to deploy you'll need a working 
Juju installation, and a successful bootstrap.

juju deploy dtn2

# Configuration

Currently it only allows point-to-point communication.


